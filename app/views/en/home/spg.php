                O que é SPG?
            </div>
            <div class="read__content__text">

                <p class="paragraph read__content__text__paragraph">
                    Os Sistemas Participativos de Garantia-SPG promovem, de forma participativa, determinadas condições
                    de produção, neste caso a produção biológica. Os SPGs são ditos participativos, porque implicam uma
                    relação estreita entre o produtor, o consumidor e os promotores dos mercados, de forma a gerar uma
                    linguagem e objetivos comuns a todos, que nos motiva a trabalhar pelo bem-estar e sustentabilidade
                    comuns. </p>

                <p class="paragraph read__content__text__paragraph">
                    O nosso SPG é um meio pelo qual se certifica que os produtores da nossa iniciativa são biológicos ou
                    determina que estão em processo de transição. No primeiro caso, significa que são produtores que
                    cumprem a definição e os princípios delineados pelo SPG em seus sistemas de produção; no segundo
                    caso, significa que estão a caminho de cumprir esses princípios com total prontidão.
                </p>


